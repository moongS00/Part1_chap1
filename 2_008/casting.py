# 정수로 형변환 : int() 함수 사용
var1 = '100'
print(type(var1))

var1 = int(var1)
print(type(var1))

a = True
print(type(a))

a = int(a)
print(type(a))
print(a)

# 실수로 형변환 : float() 함수 사용
var2 = '3.14'
print(type(var2))

var2 = float(var2)
print(type(var2))

a = float(a)
print(type(a))
print(a)